# Write a while loop that displays the integers 1 through 10. 
# first initialise a variable to start with 1
shoe = 1 
# secondly Set up a while loop to iterate as long as shoe is less than or equal to 10
while shoe <= 10:
# Display the current value of shoe
    print(shoe)
# Increment shoe by 1 for the next iteration
    shoe= shoe + 1
